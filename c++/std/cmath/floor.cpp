#include <iostream>
#include <cmath>

int main() {

    float a = 32.0f;
    float s = 10.0f;

    std::cout << a / s << std::endl;
    std::cout << std::floor(a / s)<< std::endl;


    int b = 32;
    int c = 10;

    std::cout << b / c << std::endl;
    std::cout << std::floor(b / c)<< std::endl;

    return 0;
}